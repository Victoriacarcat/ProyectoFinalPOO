    private void jLabel1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel1MouseClicked
        String nombrePersona = jTextField1.getText().trim();
        if (nombrePersona.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, ingrese un nombre.");
            return;
        }

        String idPersona = buscarIdPersonaPorNombre(nombrePersona);
        if (idPersona == null) {
            JOptionPane.showMessageDialog(this, "No se encontró la persona con ese nombre.");
            return;
        }

        Mesa mesaSeleccionada = (Mesa) jComboBox1.getSelectedItem();
        if (mesaSeleccionada == null) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar una mesa.");
            return;
        }

        String idMesa = mesaSeleccionada.getId_mesa();
        String nuevoIdReserva = generarNuevoIdReservas();

        LocalDateTime ahora = LocalDateTime.now();
        DateTimeFormatter formato = DateTimeFormatter.ofPattern("yyyy-MM-dd HH:mm:ss");
        String fechaHoraActual = ahora.format(formato);

        ingresar(nuevoIdReserva, fechaHoraActual, idPersona, idMesa);
    }//GEN-LAST:event_jLabel1MouseClicked

