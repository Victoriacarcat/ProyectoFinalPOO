    public void cargarMesasEnComboBox() {
        try (Connection conexion = Conexion.conectar();//Validamos conexión con la base
             Statement st = conexion.createStatement();
             ResultSet rs = st.executeQuery("SELECT id_mesa, capacidad, id_status FROM mesa")) {//consulta
            while (rs.next()) {//Se recorre los resultados de la consulta
                String id = rs.getString("id_mesa");
                int capacidad = rs.getInt("capacidad");
                String status = rs.getString("id_status");
                Mesa mesas = new Mesa(id, capacidad, status);//Se instancia la clase Mesa para agregar un objeto a la lista
                jComboBox1.addItem(mesas);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al cargar mesas: " + e.getMessage());
        }
    } 
    public String generarNuevoIdReservas() {
        String nuevoId = "0001";
        try (Connection conexion = Conexion.conectar();
             Statement st = conexion.createStatement();
             ResultSet rs = st.executeQuery("SELECT MAX(CAST(id_reserva AS UNSIGNED)) FROM reservas")) {
            if (rs.next()) {
                String ultimoId = rs.getString(1);
                if (ultimoId != null && !ultimoId.trim().isEmpty()) {
                    ultimoId = ultimoId.replaceAll("[^0-9]", ""); // Limpiar caracteres no numéricos y remplazamos por vacio
                    try {
                        int idNum = Integer.parseInt(ultimoId);
                        nuevoId = String.format("%04d", idNum + 1);//Aumentar los ids y convertir a string
                    } catch (NumberFormatException ex) {
                        System.out.println("Error al convertir a número: '" + ultimoId + "'");
                        ex.printStackTrace();
                    }
                }
            } else {
                System.out.println("ResultSet vacío.");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al generar ID de detalle venta: " + e.getMessage());
            e.printStackTrace();
        }

        return nuevoId;
    }
    public String buscarIdPersonaPorNombre(String nombre) {
        String idPersona = null;
        try (Connection conexion = Conexion.conectar();
             PreparedStatement ps = conexion.prepareStatement("SELECT id_personas FROM personas WHERE nombre = ?")) {
            ps.setString(1, nombre);
            ResultSet rs = ps.executeQuery();
            if (rs.next()) {
                idPersona = rs.getString("id_personas");
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error al buscar persona: " + e.getMessage());
        }
        return idPersona;
    }

    @Override
    public void ingresar(String idReserva, String fechahora, String idpersona, String idMesa) {
        try {
            Connection conexion = Conexion.conectar();
            String sql = "INSERT INTO reservas (id_reserva, fecha_hora, id_persona, id_mesa) VALUES (?, ?, ?, ?)";
            PreparedStatement st = conexion.prepareStatement(sql);
            st.setString(1, idReserva);
            st.setString(2, fechahora);
            st.setString(3, idpersona);
            st.setString(4, idMesa);
            st.executeUpdate();

            JOptionPane.showMessageDialog(this, "Reserva registrada correctamente.");
            st.close();
            conexion.close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error al registrar reserva: " + e.getMessage());
        }
    }
    
